<article class="container">
	<header>
		<h1>Blogging is hard</h1>
		<p class="entry-meta">
			Posted on
			<time class="posted-on" datetime="2022-09-22T17:47:17-04:00">September 22, 2022</time>
		</p>
	</header>
	<p>
		Since I’ve redone my website almost a year ago, I literally haven’t blogged once. I keep looking
		at it, thinking it’s sad that I don’t use it. I like writing, even though I don’t do it a lot. I
		think I’ve identified two of the biggest sources of the problem for me.
	</p>
	<h2>Topics</h2>
	<p>
		I think part of the problem is that I have a hard time coming up with topics that I find
		relevant, when in reality, it’s a non-issue. This is a personal website, not a tech blog or
		something that <em>needs</em> relevant content and huge traffic. I mean honestly, I know for a fact
		that there’s almost nobody that reads this. And that’s completely fine. I could (and probably should)
		just write about stuff I want to, even if it’s not tech-related or anything.
	</p>
	<h2>Motivation</h2>
	<p>
		The other big problem for me is motivation. I find that I’m quite prone to seasonal depression,
		so during winter, I can get a little less motivated to do, well… basically anything. Gaming,
		reading, working on projects, school, work, you name it. I just can’t get motivated sometimes,
		and it <em>really sucks</em>. I’m not sure how to fix this particular problem though. I could
		just force myself to do it, but then I feel like I might not enjoy it, which would probably make
		the problem even worse.
	</p>
	<p>
		Anyway, right now I really want to start blogging more, so I think that’s what I’ll try to do in
		the near future. To all the tens of people reading this, I’m happy you’re here and uh yeah! Hope
		you’ll be back when I’ll finally make a third blog post!
	</p>
